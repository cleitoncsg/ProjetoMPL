package controle;

import modelo.Motor;
import modelo.MotorFerro;
import modelo.Pneu;
import modelo.PneuBorracha;
import modelo.Roda;
import modelo.RodaAco;

public class MetalurgicaBrasileira extends Metalurgica{

	@Override
	public Motor criarMotor() {
		MotorFerro motorFerro = new MotorFerro();
		return motorFerro;
	}

	@Override
	public Roda criarRoda() {
		RodaAco rodaAco = new RodaAco();
		return rodaAco;
	}

	@Override
	public Pneu criarPneu() {
		PneuBorracha pneuBorracha = new PneuBorracha();
		return pneuBorracha;
	}

}
